package beans;

/**
 *
 * @author Ilija
 */
public class Dogadjaj {
    
    private String tip;
    private int idKonf;
    private int idSesija;
    private String vremePoc;
    private String vremeKraj;
    private String datumPoc;
    private String sala;
    private String naziv;

    public Dogadjaj() {
    }
    
    

    public String getTip() {
        return tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }

    public int getIdKonf() {
        return idKonf;
    }

    public void setIdKonf(int idKonf) {
        this.idKonf = idKonf;
    }

    public int getIdSesija() {
        return idSesija;
    }

    public void setIdSesija(int idSesija) {
        this.idSesija = idSesija;
    }

    public String getVremePoc() {
        return vremePoc;
    }

    public void setVremePoc(String vremePoc) {
        this.vremePoc = vremePoc;
    }

    public String getVremeKraj() {
        return vremeKraj;
    }

    public void setVremeKraj(String vremeKraj) {
        this.vremeKraj = vremeKraj;
    }

    public String getDatumPoc() {
        return datumPoc;
    }

    public void setDatumPoc(String datumPoc) {
        this.datumPoc = datumPoc;
    }

    public String getSala() {
        return sala;
    }

    public void setSala(String sala) {
        this.sala = sala;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }
    
    
    
}
